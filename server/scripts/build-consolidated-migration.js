import { readdir, readFile, mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const serverRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(serverRoot, "..");
const migrationsDir = path.join(repoRoot, "supabase", "migrations");
const hardeningPath = path.join(serverRoot, "db", "hardening.sql");
const outPath = path.join(serverRoot, "db", "consolidated.sql");

const migrationFiles = (await readdir(migrationsDir))
  .filter((file) => file.endsWith(".sql"))
  .sort((a, b) => a.localeCompare(b));

if (migrationFiles.length === 0) {
  throw new Error(`No SQL migrations found in ${migrationsDir}`);
}

const chunks = [
  "-- JAAD CLOUD consolidated Supabase migration",
  "-- Generated from supabase/migrations plus server/db/hardening.sql.",
  "-- Do not edit this file directly; update source migrations or hardening.sql instead.",
  "",
];

for (const file of migrationFiles) {
  const sql = await readFile(path.join(migrationsDir, file), "utf8");
  chunks.push(
    `-- ============================================================================`,
    `-- Migration: ${file}`,
    `-- ============================================================================`,
    sql.trimEnd(),
    "",
  );
}

const hardeningSql = await readFile(hardeningPath, "utf8");
chunks.push(
  `-- ============================================================================`,
  `-- Final hardening layer`,
  `-- ============================================================================`,
  hardeningSql.trimEnd(),
  "",
);

await mkdir(path.dirname(outPath), { recursive: true });
await writeFile(outPath, chunks.join("\n"), "utf8");

console.log(`Wrote ${outPath}`);
console.log(`Included ${migrationFiles.length} migrations and hardening.sql`);
